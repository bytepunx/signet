// TestNormalizeForJSON verifies that yaml.v3 date values are converted to strings.
func TestNormalizeForJSON(t *testing.T) {
	cases := []struct {
		name  string
		input interface{}
		check func(t *testing.T, got interface{})
	}{
		{
			name:  "string passthrough",
			input: "hello",
			check: func(t *testing.T, got interface{}) { assert.Equal(t, "hello", got) },
		},
		{
			name:  "int passthrough",
			input: 42,
			check: func(t *testing.T, got interface{}) { assert.Equal(t, 42, got) },
		},
		{
			name:  "nested map recurses",
			input: map[string]interface{}{"a": map[string]interface{}{"b": 1}},
			check: func(t *testing.T, got interface{}) {
				m := got.(map[string]interface{})
				inner := m["a"].(map[string]interface{})
				assert.Equal(t, 1, inner["b"])
			},
		},
		{
			name:  "slice recurses",
			input: []interface{}{"x", 2},
			check: func(t *testing.T, got interface{}) {
				s := got.([]interface{})
				assert.Equal(t, "x", s[0])
				assert.Equal(t, 2, s[1])
			},
		},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			tc.check(t, normalizeForJSON(tc.input))
		})
	}
}
